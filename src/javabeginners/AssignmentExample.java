
package javabeginners;

public class AssignmentExample {
    public static void main(String[] args) {
        
        int x = 2;
        int y = 5;
        
        x += y; // x = x+y
        System.out.println("x = "+x);   
        
        x -= y; // x = x-y
        System.out.println("x = "+x);   
        
        x *= y; // x = x*y
        System.out.println("x = "+x);   
        
        x /= y; // x = x/y
        System.out.println("x = "+x);   
        
        x %= y; // x = x%y
        System.out.println("x = "+x);   
    }
}
